
const opcao = parseInt(prompt("Escolha uma opção:\n1. Opção 1\n2. Opção 2\n3. Opção 3"));

switch (opcao) {
  case 1:
    alert("Opção 1 escolhida");
    break;
  case 2:
    alert("Opção 2 escolhida");
    break;
  case 3:
    alert("Opção 3 escolhida");
    break;
  default:
    alert("Opção inválida");
}